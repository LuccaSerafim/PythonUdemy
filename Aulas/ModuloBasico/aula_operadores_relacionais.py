"""
 Aula de operadores relacionais(Comparação)
 Operadores: maior, maior ou igual, menor, menor ou igual, igual e diferente
 Caracteres deles: 
 Maior(>). Maior ou igual(>=)
 Menor(<). Menor ou igual(<=)
 Igual(==)
 Diferente(!=)
 OBS1: Ao usar qualquer operador de comparação você recebe com resposta um boolean(True ou False)  
 OBS2: Se você usar apenas um = você esta fazendo atribuição a algo. O sinal para igualar mesmo são ==
 OBS3: Geralmente usamos os operadores de comparação com numeros, mas nem sempre.

"""

maior = 2 > 0
print(maior)

maior_ou_igual = 2 >= 2
print(maior_ou_igual)

menor = 10 < 20
print(menor)

menor_ou_igual = 5 <= 10
print(menor_ou_igual)

igual = 100 == 100
print(igual)

diferente = 10 != 20
print(diferente)
